public class While {
    public static void main(String[] args) {
        int a = 0;
        while (a < 11) {
            if (a < 11) {
                System.out.println( a + " сейчас длобавлю плюс один.");
                a++;
            } else {
                System.out.println("the end");
            }
        }
    }
}
